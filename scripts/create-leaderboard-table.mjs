import pg from 'pg';

// Using the standard PostgreSQL connection string from your Supabase dashboard
// Go to Supabase Dashboard -> Project Settings -> Database -> Connection string -> Nodejs
const connectionString = process.env.DATABASE_URL;

if (!connectionString) {
  console.error("❌ Missing DATABASE_URL.");
  console.error("Please run the script by passing your database connection string as an environment variable:");
  console.error("Example: DATABASE_URL=\"postgres://postgres.[project-ref]:[password]@aws-0-[region].pooler.supabase.com:6543/postgres\" node scripts/create-leaderboard-table.mjs");
  process.exit(1);
}

const client = new pg.Client({
  connectionString,
});

async function createTable() {
  try {
    console.log("Connecting to Supabase PostgreSQL database...");
    await client.connect();

    console.log("Running CREATE TABLE statement...");
    const query = `
      CREATE TABLE IF NOT EXISTS leaderboard (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        name TEXT NOT NULL,
        score INTEGER NOT NULL,
        game TEXT DEFAULT 'unknown' NOT NULL,
        date TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
      );
    `;

    await client.query(query);
    console.log("✅ Success! The 'leaderboard' table was created or already exists.");

    // Verify it exists by fetching columns
    const { rows } = await client.query(`
      SELECT column_name, data_type 
      FROM information_schema.columns 
      WHERE table_name = 'leaderboard';
    `);

    console.log("Table Schema:");
    console.table(rows);

  } catch (error) {
    console.error("❌ Error creating table:");
    console.error(error.message);
  } finally {
    await client.end();
  }
}

createTable();
